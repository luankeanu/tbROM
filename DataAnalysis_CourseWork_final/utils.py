### %E AVG CALC ###
"""
calculates the average percentage error between true and predicted values;
this will be used in the analysis part to see if some modules are more accurate for certain variables
    compared to others
"""

def calculate_average_percentage_error(y_true, y_pred):
    y_true = np.array(y_true)
    y_pred = np.array(y_pred)
    non_zero_mask = y_true != 0
    ape = np.abs((y_true[non_zero_mask] - y_pred[non_zero_mask]) / y_true[non_zero_mask]) * 100
    return np.mean(ape) if ape.size > 0 else 0




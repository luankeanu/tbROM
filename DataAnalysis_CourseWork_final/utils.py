def get_linear_regression_coefficients(model, feature_names):
    coefficients = model.coef_
    feature_importance = {}
    for i, feature in enumerate(feature_names):
        avg_abs_coef = np.mean(np.abs(coefficients[:, i]))
        feature_importance[feature] = avg_abs_coef
    return dict(sorted(feature_importance.items(), key=lambda item: item[1], reverse=True))

def get_custom_linear_regression_coefficients(model, feature_names):
    """Extracts coefficients from your custom Linear Regression model."""
    if model.coef_ is not None and len(model.coef_) == len(feature_names) + 1:
        # The first element of coef_ is the intercept
        coefficients = model.coef_[1:]
        return dict(zip(feature_names, coefficients))
    else:
        return {}

